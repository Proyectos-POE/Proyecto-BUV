public class AreaConforma
{
    private int codigoArea1;
    private int codigoArea2;

    public AreaConforma(int codigoArea1, int codigoArea2)
    {
        this.codigoArea1 = codigoArea1;
        this.codigoArea2 = codigoArea2;
    }

    public int getCodigoArea1()
    {
        return codigoArea1;
    }

    public int getCodigoArea2()
    {
        return codigoArea2;
    }
}
